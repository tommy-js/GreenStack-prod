export function findStocks(arg: string) {
  let lowerCaseArg = arg.toLowerCase();
  let returnArray = [];
  for (let i = 0; i < companyList.length; i++) {
    let titleToLower = companyList[i].title.toLowerCase();
    let tickerToLower = companyList[i].ticker.toLowerCase();
    let descriptionToLower = companyList[i].description.toLowerCase();
    if (
      titleToLower.includes(lowerCaseArg) ||
      tickerToLower.includes(arg) ||
      descriptionToLower.includes(arg)
    ) {
      returnArray.push(companyList[i]);
    }
  }
  return returnArray;
}

export function findSelectStocks(paramObj: any) {
  let returnArray = [];
  if (paramObj.select1 === "Any" && paramObj.select2 === "Any") {
    return companyList;
  } else if (paramObj.select1 === "Any" && paramObj.select2 != "Any") {
    let arg = paramObj.select2.toLowerCase();
    for (let i = 0; i < companyList.length; i++) {
      let countryToLower = companyList[i].country.toLowerCase();
      if (countryToLower.includes(arg)) {
        returnArray.push(companyList[i]);
      }
    }
  } else if (paramObj.select1 != "Any" && paramObj.select2 === "Any") {
    let arg = paramObj.select1.toLowerCase();
    for (let i = 0; i < companyList.length; i++) {
      let sectorToLower = companyList[i].sector.toLowerCase();
      if (sectorToLower.includes(arg)) {
        returnArray.push(companyList[i]);
      }
    }
  } else if (paramObj.select1 != "Any" && paramObj.select2 != "Any") {
    // Sector
    let arg1 = paramObj.select1.toLowerCase();
    // Country
    let arg2 = paramObj.select2.toLowerCase();
    for (let i = 0; i < companyList.length; i++) {
      let countryToLower = companyList[i].sector.toLowerCase();
      if (countryToLower.includes(arg1)) {
        returnArray.push(companyList[i]);
      }
    }
    let shortenedArray = [];
    for (let j = 0; j < returnArray.length; j++) {
      let sectorToLower = returnArray[j].country.toLowerCase();
      if (sectorToLower.includes(arg2)) {
        shortenedArray.push(returnArray[j]);
      }
    }
    returnArray = shortenedArray;
  }

  return returnArray;
}
